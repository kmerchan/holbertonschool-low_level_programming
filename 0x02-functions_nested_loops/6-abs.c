#include <stdlib.h>

/**
 * main - Entry point
 * function will print sign of character
 * Return: Always 0 (Success)
 */

int _abs(int)
{
	int n;

	if (n > 0)
	{
		/* if n is positive, print n */
		return ('n');
	}
	else if (n < 0)
	{
		/* if n is negative, remove - */
		return ('n');
	}
	else
	{
		/* if n is zero, print 0 */
		return (0);
	}
}
